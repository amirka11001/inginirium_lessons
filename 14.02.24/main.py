import pygame

pygame.init()

width = 500
height = 500

win = pygame.display.set_mode((width, height))

class Circle:
    def __init__(self,colour,x,y,rad):
        self.colour=colour
        self.x=x
        self.y=y
        self.rad=rad
        self.is_jump=False
        self.jump_counter=15

    def draw(self):
        pygame.draw.circle(win,self.colour,(self.x,self.y),self.rad)

    def move_by_keys(self):
        keys = pygame.key.get_pressed()
        if keys[pygame.K_LEFT]:
            self.x -= 1
        if keys[pygame.K_RIGHT]:
            self.x += 1
        if keys[pygame.K_UP]:
            self.y -= 1
        if keys[pygame.K_DOWN]:
            self.y += 1
    def jump(self):
        keys=pygame.key.get_pressed()
        if keys[pygame.K_SPACE]:
            self.is_jump=True
        if self.is_jump:
            if self.jump_counter>=-15:
                self.y-=self.jump_counter
                self.jump_counter-=2
            else:
                self.jump_counter=15
                self.is_jump=False


krug = Circle((255, 0, 0),10,10,30)
FPS=60
clock=pygame.time.Clock()

while True:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            exit()

    win.fill((255, 255, 255))
    krug.draw()
    krug.move_by_keys()
    krug.jump()
    pygame.display.update()
    clock.tick(FPS)
    print(clock.get_fps())
    pygame.time.delay(10)





























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































